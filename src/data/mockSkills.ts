import type { ClassName, SkillConfig } from "@/types/game";

type SkillSeed = Omit<
  SkillConfig,
  | "class_tier"
  | "boss_modifier"
  | "use_character_element"
  | "ignore_taunt"
  | "can_crit"
  | "can_miss"
>;

function classSkill(seed: SkillSeed): SkillConfig {
  return {
    ...seed,
    class_tier: 1,
    boss_modifier: 1,
    use_character_element: false,
    ignore_taunt: false,
    can_crit: seed.skill_type !== "heal" && seed.skill_type !== "shield",
    can_miss: seed.skill_type === "attack" || seed.skill_type === "debuff",
  };
}

